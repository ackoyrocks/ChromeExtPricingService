
package com.web.price.pojo;

public class Link {

	private String rel;
	private String href;

	/**
	 * 
	 * @return The rel
	 */
	public String getRel() {
		return rel;
	}

	/**
	 * 
	 * @param rel
	 *            The rel
	 */
	public void setRel(String rel) {
		this.rel = rel;
	}

	/**
	 * 
	 * @return The href
	 */
	public String getHref() {
		return href;
	}

	/**
	 * 
	 * @param href
	 *            The href
	 */
	public void setHref(String href) {
		this.href = href;
	}

}
