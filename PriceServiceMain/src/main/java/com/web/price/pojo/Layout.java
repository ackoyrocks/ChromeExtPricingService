
package com.web.price.pojo;

public class Layout {

	private String type;
	private String name;
	private ContentMapping contentMapping;

	/**
	 * 
	 * @return The type
	 */
	public String getType() {
		return type;
	}

	/**
	 * 
	 * @param type
	 *            The type
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * 
	 * @return The name
	 */
	public String getName() {
		return name;
	}

	/**
	 * 
	 * @param name
	 *            The name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * 
	 * @return The contentMapping
	 */
	public ContentMapping getContentMapping() {
		return contentMapping;
	}

	/**
	 * 
	 * @param contentMapping
	 *            The contentMapping
	 */
	public void setContentMapping(ContentMapping contentMapping) {
		this.contentMapping = contentMapping;
	}

}
